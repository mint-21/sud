int		g_matrix[9][9];

void	make_int_mass(char **argv)
{
	int	i;
	int	j;

	i = 1;
	j = 0;
	while (i <= 9)
	{
		j = 0;
		while (j < 9)
		{
			if (argv[i][j] >= '1' && argv[i][j] <= '9')
				g_matrix[i - 1][j] = argv[i][j] - '0';
			else
				g_matrix[i - 1][j] = 0;
			j++;
		}
		i++;
	}
}
